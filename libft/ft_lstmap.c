#include "libft.h"

t_list		*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list		*res;

	if (!lst)
		return (0);
	else
	{
		res = (t_list*)malloc(sizeof(f(lst)));
		res = f(lst);
		res->next = ft_lstmap(lst->next, f);
		return (res);
	}
	return (0);
}
