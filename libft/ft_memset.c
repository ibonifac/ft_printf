#include <string.h>

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*s;

	if (len == 0)
		return (b);
	s = (unsigned char *)b;
	while (len--)
	{
		*s = (unsigned char)c;
		if (len)
			s++;
	}
	return (b);
}
